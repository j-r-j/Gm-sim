/**
 * Interface for the name generator
 */
export interface NameGenerator {
  generateFirstName(): string;
  generateLastName(): string;
  generateFullName(): { firstName: string; lastName: string };
}

/**
 * Creates a new name generator instance.
 * @returns A NameGenerator instance
 */
export function createNameGenerator(): NameGenerator {
  return {
    generateFirstName(): string {
      return randomElement(FIRST_NAMES);
    },

    generateLastName(): string {
      return randomElement(LAST_NAMES);
    },

    generateFullName(): { firstName: string; lastName: string } {
      return {
        firstName: this.generateFirstName(),
        lastName: this.generateLastName(),
      };
    },
  };
}

/**
 * Generates a random first name.
 * @returns A random first name
 */
export function generateFirstName(): string {
  return randomElement(FIRST_NAMES);
}

/**
 * Generates a random last name.
 * @returns A random last name
 */
export function generateLastName(): string {
  return randomElement(LAST_NAMES);
}

/**
 * Generates a full name (first and last).
 * @returns An object with firstName and lastName
 */
export function generateFullName(): { firstName: string; lastName: string } {
  return {
    firstName: generateFirstName(),
    lastName: generateLastName(),
  };
}
